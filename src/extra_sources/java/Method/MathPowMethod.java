package extra_sources.java.Method;

public class MathPowMethod {
    public static void main(String[] args) {
       // In Java, the Math.pow method is used to calculate the power of a number. It takes two parameters: the base and the exponent. The Math.pow method signature is as follows:

           double base = 2.0;  //base number
           double exponent = 3.0; //the power to which the base is raised

           double result = Math.pow(base, exponent);

           System.out.println(base + " raised to the power of " + exponent + " is: " + result);


        //In this example, it calculates 2.0 raised to the power of 3.0, and the result is printed. The output will be: 2.0 raised to the power of 3.0 is: 8.0

        System.out.println("Math.pow(5,2) = " + Math.pow(5, 2));

    }
}
